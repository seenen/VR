using UnityEngine;
using System.Collections;

public class _Vector3
{
    public float X;
    public float Y;
    public float Z;

    public _Vector3()
    {
        X = 0;
        Y = 0;
        Z = 0;
    }

    public _Vector3(float x, float y, float z)
    {
        X = x;
        Y = y;
        Z = z;
    }
}
